function [P,E,V] = simulateNets (net1,net2,net3)

    P = zeros(100,1);
    E = zeros(100,1);
    V = zeros(100,1);

    [~,T1,~,T2,~,T3] = getData();

    P(1:10,1) = T1(1:10,89);
    E(1:10,1) = T2(1:10,89);
    V(1:10,1) = T3(1:10,89);

    for i=1:9
        P(i*10+1:(i+1)*10,1) = sim(net1,[P((i-1)*10+1:i*10,1); E((i-1)*10+1:i*10,1); V((i-1)*10+1:i*10,1)]);
        E(i*10+1:(i+1)*10,1) = sim(net2,[E((i-1)*10+1:i*10,1); V((i-1)*10+1:i*10,1); P((i-1)*10+1:i*10,1)]);
        V(i*10+1:(i+1)*10,1) = sim(net3,[V((i-1)*10+1:i*10,1); E((i-1)*10+1:i*10,1); P((i-1)*10+1:i*10,1)]);
    end

    P = P(11:end);
    E = E(11:end);
    V = V(11:end);
    
end

